class PathFinderLevel {
  final int id;
  final List<String> maze;
  final Point start;
  final Point end;

  PathFinderLevel({
    required this.id,
    required this.maze,
    required this.start,
    required this.end,
  });
}

class Point {
  int r;
  int c;

  Point(this.r, this.c);
  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is Point &&
          runtimeType == other.runtimeType &&
          r == other.r &&
          c == other.c;

  @override
  int get hashCode => r.hashCode ^ c.hashCode;
}
